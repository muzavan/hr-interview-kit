def countingValleys(steps, path):
    # Write your code here
    cnt = 0
    lvl = 0
    for p in path:
        if p == 'U':
            lvl += 1
            continue

        # p == 'D'
        if lvl == 0:
            cnt += 1
        
        lvl -= 1

    return cnt

